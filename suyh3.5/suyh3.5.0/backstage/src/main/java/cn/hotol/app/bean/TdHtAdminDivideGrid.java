public class TdHtAdminDivideGrid {

    private int grid_id;//划分区域坐标点id
    private int divide_id;//划分的区域id
    private BigDecimal grid_longitude;//坐标经度
    private BigDecimal grid_latitude;//坐标纬度
    private int grid_sort;//同一区域坐标点排序
    private int grid_state;//坐标点状态（1：无效；0有效；）

    public int getGrid_id() {
        return grid_id;
    }

    public void setGrid_id(int grid_id) {
        this.grid_id = grid_id;
    }

    public int getDivide_id() {
        return divide_id;
    }

    public void setDivide_id(int divide_id) {
        this.divide_id = divide_id;
    }

    public BigDecimal getGrid_longitude() {
        return grid_longitude;
    }

    public void setGrid_longitude(BigDecimal grid_longitude) {
        this.grid_longitude = grid_longitude;
    }

    public BigDecimal getGrid_latitude() {
        return grid_latitude;
    }

    public void setGrid_latitude(BigDecimal grid_latitude) {
        this.grid_latitude = grid_latitude;
    }

    public int getGrid_sort() {
        return grid_sort;
    }

    public void setGrid_sort(int grid_sort) {
        this.grid_sort = grid_sort;
    }

    public int getGrid_state() {
        return grid_state;
    }

    public void setGrid_state(int grid_state) {
        this.grid_state = grid_state;
    }
}

public class TsHtMenu {

    private int menu_id;//主标示
    private String menu_name;//菜单名称
    private int parent_id;//父标示--数据字段中数据存在上下级的关系
    private int menu_sort;//排序
    private int is_valid;//是否可用
    private int left_id;//左节点
    private int right_id;//右节点
    private int is_list;//是否列表

    public int getMenu_id() {
        return menu_id;
    }

    public void setMenu_id(int menu_id) {
        this.menu_id = menu_id;
    }

    public String getMenu_name() {
        return menu_name;
    }

    public void setMenu_name(String menu_name) {
        this.menu_name = menu_name;
    }

    public int getParent_id() {
        return parent_id;
    }

    public void setParent_id(int parent_id) {
        this.parent_id = parent_id;
    }

    public int getMenu_sort() {
        return menu_sort;
    }

    public void setMenu_sort(int menu_sort) {
        this.menu_sort = menu_sort;
    }

    public int getIs_valid() {
        return is_valid;
    }

    public void setIs_valid(int is_valid) {
        this.is_valid = is_valid;
    }

    public int getLeft_id() {
        return left_id;
    }

    public void setLeft_id(int left_id) {
        this.left_id = left_id;
    }

    public int getRight_id() {
        return right_id;
    }

    public void setRight_id(int right_id) {
        this.right_id = right_id;
    }

    public int getIs_list() {
        return is_list;
    }

    public void setIs_list(int is_list) {
        this.is_list = is_list;
    }
}

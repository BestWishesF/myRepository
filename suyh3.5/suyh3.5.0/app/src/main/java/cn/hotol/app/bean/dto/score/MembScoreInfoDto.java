public class MembScoreInfoDto {

    private int my_score;//用户积分
    private int is_sign;//是否签到
    private String ranking;//排名
    private int sign_score;//签到积分奖励

    public int getMy_score() {
        return my_score;
    }

    public void setMy_score(int my_score) {
        this.my_score = my_score;
    }

    public int getIs_sign() {
        return is_sign;
    }

    public void setIs_sign(int is_sign) {
        this.is_sign = is_sign;
    }

    public String getRanking() {
        return ranking;
    }

    public void setRanking(String ranking) {
        this.ranking = ranking;
    }

    public int getSign_score() {
        return sign_score;
    }

    public void setSign_score(int sign_score) {
        this.sign_score = sign_score;
    }
}

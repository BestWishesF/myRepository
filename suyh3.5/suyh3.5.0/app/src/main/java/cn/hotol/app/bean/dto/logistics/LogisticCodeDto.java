public class LogisticCodeDto {

    private String waybill_number;//快递单号
    private String shipper_code;//快递编码
    private String shipper_name;//快递名称
    private int state;//快递状态

    private List<LogisticsInfoDto> logistics_info_dto_list;//物流信息

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public String getWaybill_number() {
        return waybill_number;
    }

    public void setWaybill_number(String waybill_number) {
        this.waybill_number = waybill_number;
    }

    public String getShipper_code() {
        return shipper_code;
    }

    public void setShipper_code(String shipper_code) {
        this.shipper_code = shipper_code;
    }

    public String getShipper_name() {
        return shipper_name;
    }

    public void setShipper_name(String shipper_name) {
        this.shipper_name = shipper_name;
    }

    public List<LogisticsInfoDto> getLogistics_info_dto_list() {
        return logistics_info_dto_list;
    }

    public void setLogistics_info_dto_list(List<LogisticsInfoDto> logistics_info_dto_list) {
        this.logistics_info_dto_list = logistics_info_dto_list;
    }
}

package cn.hotol.app.bean.dto.gem;

import java.math.BigDecimal;

/**
 * Created by Administrator on 2017-04-12.
 */
public class GemDto {

    /** 奖品名称 */
    private BigDecimal name;
    /** 奖品概率 */
    private int priority;

    public GemDto() {
        super();
    }

    public GemDto(BigDecimal name, int priority) {
        super();
        this.name = name;
        this.priority = priority;
    }

    @Override
    public String toString() {
        return "Gum [name=" + name + ", priority=" + priority + "]";
    }
    public BigDecimal getName() {
        return name;
    }

    public void setName(BigDecimal name) {
        this.name = name;
    }
    public int getPriority() {
        return priority;
    }
    public void setPriority(int priority) {
        this.priority = priority;
    }

}
